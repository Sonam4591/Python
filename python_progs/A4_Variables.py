# Multi Words Variable Names

#1.Camel Case
#Each word, except the first, starts with a capital letter:

myVariableName = "Kashy"
print(myVariableName);

#2.Pascal Case
#Each word starts with a capital letter:

MyVariableName = "Sonam"
